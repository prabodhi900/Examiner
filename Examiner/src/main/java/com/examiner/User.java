package com.examiner;


public class User {
    private int id;
    private String name;
    private String grade;
    private String nic;
	public User(int id, String name, String grade, String nic) {
		super();
		this.id = id;
		this.name = name;
		this.grade = grade;
		this.nic = nic;
	}
	public int getId() {
		return id;
	}
	
	public String getName() {
		return name;
	}
	
	public String getGrade() {
		return grade;
	}
	
	public String getNic() {
		return nic;
	}
	
    
    
    
   
	

	  
}
